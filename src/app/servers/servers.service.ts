export class ServersService {
  private servers = [
    {
      id: 1,
      name: 'Productionserver',
      status: 'online'
    },
    {
      id: 2,
      name: 'Testserver',
      status: 'offline'
    },
    {
      id: 3,
      name: 'Devserver',
      status: 'offline'
    }
  ];

  getServers() {
    return this.servers.slice(); 
    // shallow copy, original objects remain the same, only the references copy,
    // hence it is okay to pass clone(), since we are only performing update on the three servers objects.
    // If you would also be adding a new server, than this would not work,
    // since it will only return once in the ngOnInit of ServersComponent.
    // In that case you need to explicitly emit each time a new server gets added!
  }

  getServer(id: number) {
    const server = this.servers.find(
      (s) => {
        return s.id === id;
      }
    );
    return server;
  }

  updateServer(id: number, serverInfo: {name: string, status: string}) {
    const server = this.servers.find(
      (s) => {
        return s.id === id;
      }
    );
    if (server) {
      server.name = serverInfo.name;
      server.status = serverInfo.status;
    }
  }
}
